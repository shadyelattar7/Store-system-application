    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        try {
            String Traget = JOptionPane.showInputDialog(null, "Enter Traget");
            FileInputStream f2 = new FileInputStream("D:\\Store.txt");
            DataInputStream dis = new DataInputStream(f2);
            FileOutputStream f3 = new FileOutputStream("D:\\Store2.txt");
            DataOutputStream dos2 = new DataOutputStream(f3);

            while (dis.available() > 0) {
                String ID = dis.readUTF();
                String Name = dis.readUTF();
                String q = dis.readUTF();
                String p = dis.readUTF();

                if (!Name.trim().equals(Traget)) {
                    dos2.writeUTF("\n" + ID);
                    dos2.writeUTF("\n" + Name);
                    dos2.writeUTF("\n" + q);
                    dos2.writeUTF("\n" + p);

                } else {
                    String newq = JOptionPane.showInputDialog(null, "Enter new Quantity");
                    String newp = JOptionPane.showInputDialog(null, "Enter new Price");
                    dos2.writeUTF("\n" + ID);
                    dos2.writeUTF("\n" + Name);
                    dos2.writeUTF("\n" + newq);
                    dos2.writeUTF("\n" + newp);
                }
            }

            f2.close();
            dis.close();
            f3.close();
            dos2.close();
            File f = new File("E:\\Store.txt");
            f.delete();
            File frename = new File("E:\\Store2.txt");
            frename.renameTo(f);
        } catch (Exception e) {

        }


    }//GEN-LAST:event_jButton9ActionPerformed

