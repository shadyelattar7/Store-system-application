    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        ///////////// create an button belong to product and estmit 
        a1 = new ArrayList<>();
        buttons = new ArrayList<>();
    
        try {
            FileInputStream f1 = new FileInputStream("D:\\Store.txt");
            DataInputStream dis = new DataInputStream(f1);
            while (dis.available() > 0) {
                Productmarket pro = new Productmarket();
                pro.ID = dis.readUTF();
                pro.Name = dis.readUTF();
                pro.Quantity = (Double.parseDouble(dis.readUTF()));
                pro.UnPrice = Double.parseDouble(dis.readUTF());
                a1.add(pro);
            }
            dis.close();
            f1.close();
        } catch (Exception e) {

        }
        double[] x = new double[a1.size()]; // array to save quantity 

        for (int i = 0; i < a1.size(); i++) {
            JButton b = new JButton();

            b.setText(String.valueOf(a1.get(i).Name));
            b.setBackground(Color.green);
            b.setBounds(i * 100, 100, 80, 50);
            this.getContentPane().add(b);
            this.repaint();
            buttons.add(b);
            x[i] = a1.get(i).Quantity; // array to save quantity 
            b.addActionListener(new ActionListener() {
                @Override
                public void actionPerformed(ActionEvent e) {
                    JButton a = (JButton) e.getSource();
                    String Name = a.getText();
                    String ID = "";
                    double Quantity = 0;
                    double UnPrice = 0;
                    boolean inStore = false;
                    try {
                        for (int i = 0; i < a1.size(); i++) {
                            if (a1.get(i).Name.equals(Name)) {
                                //  JOptionPane.showMessageDialog(null,x[i]);
                                Quantity = Double.parseDouble(JOptionPane.showInputDialog(null, "Enter Quantity"));
                                if (a1.get(i).Quantity >= Quantity) {
                                    a1.get(i).Quantity = a1.get(i).Quantity - Quantity;

                                    if ((x[i] / 2) >= a1.get(i).Quantity) {
                                        b.setBackground(Color.YELLOW);
                                    }

                                    if ((x[i] / 4) >= a1.get(i).Quantity) {
                                        b.setBackground(Color.RED);
                                        JOptionPane.showMessageDialog(null, "Please Bring :" + a1.get(i).Name + "\n" + " Reset of Quantity =" + a1.get(i).Quantity);
                                    }

                                    ID = a1.get(i).ID;
                                    UnPrice = a1.get(i).UnPrice;

                                    FileOutputStream f1 = new FileOutputStream("E:\\inStore.txt", true);
                                    DataOutputStream dos = new DataOutputStream(f1);
                                    dos.writeUTF("\n" + ID);
                                    dos.writeUTF("\n" + Name);
                                    dos.writeUTF("\n" + a1.get(i).Quantity);
                                    dos.writeUTF("\n" + UnPrice);
                                    dos.close();
                                    f1.close();
                                    inStore = true;
                                }
                            }
                        }
                        if (inStore == false) {
                            JOptionPane.showMessageDialog(null, "3ady 3lana b3den");
                        }

                    } catch (Exception ea) {
                    }
                }
            });

        }

        if (a1.isEmpty()) {
            JOptionPane.showMessageDialog(null, "No DATA !");
        }

    }//GEN-LAST:event_jButton3ActionPerformed
